# FK — Frame Kernel
# ---------------------------------------------------------------------------

def generate_fk(mission_name: str, sc_id: int, body_axes: str,
                instruments: list[InstrumentDef] | None = None) -> str:
    frame_name = f"{mission_name}_SC_BODY"
    frame_id = sc_id * 1000

    lines = [
        "KPL/FK",
        "",
        "\\begintext",
        f"  Frame kernel for {mission_name}",
        f"  SC NAIF ID: {sc_id}",
        f"  Generated by BEPI on {datetime.utcnow().isoformat()}",
        "",
        "\\begindata",
        "",
        f"  FRAME_{frame_name}            = {frame_id}",
        f"  FRAME_{frame_id}_NAME         = '{frame_name}'",
        f"  FRAME_{frame_id}_CLASS        = 3",
        f"  FRAME_{frame_id}_CLASS_ID     = {frame_id}",
        f"  FRAME_{frame_id}_CENTER       = {sc_id}",
        f"  CK_{frame_id}_SCLK            = {sc_id}",
        f"  CK_{frame_id}_SPK             = {sc_id}",
        "",
        f"  OBJECT_{sc_id}_FRAME          = '{frame_name}'",
        "",
    ]

    if instruments:
        for inst in instruments:
            inst_id = sc_id - inst.naif_id_offset
            inst_frame = f"{mission_name}_{inst.abbrev}"
            inst_frame_id = inst_id * 1000
            a1, a2, a3 = inst.frame_angles_deg
            ax1, ax2, ax3 = inst.frame_axes

            lines += [
                f"  FRAME_{inst_frame}            = {inst_frame_id}",
                f"  FRAME_{inst_frame_id}_NAME     = '{inst_frame}'",
                f"  FRAME_{inst_frame_id}_CLASS    = 4",
                f"  FRAME_{inst_frame_id}_CLASS_ID = {inst_frame_id}",
                f"  FRAME_{inst_frame_id}_CENTER   = {inst_id}",
                f"  TKFRAME_{inst_frame_id}_RELATIVE = '{frame_name}'",
                f"  TKFRAME_{inst_frame_id}_SPEC     = 'ANGLES'",
                f"  TKFRAME_{inst_frame_id}_ANGLES   = ( {a1}, {a2}, {a3} )",
                f"  TKFRAME_{inst_frame_id}_AXES     = ( {ax1}, {ax2}, {ax3} )",
                f"  TKFRAME_{inst_frame_id}_UNITS    = 'DEGREES'",
                "",
                f"  NAIF_BODY_NAME += ( '{mission_name}_{inst.name}', '{inst_id}' )",
                f"  NAIF_BODY_CODE += ( {inst_id} )",
                "",
            ]

    lines += [
        f"  NAIF_BODY_NAME += ( '{mission_name}' )",
        f"  NAIF_BODY_CODE += ( {sc_id} )",
        "",
    ]

    return "\n".join(lines)


# ---------------------------------------------------------------------------
# IK — Instrument Kernel
# ---------------------------------------------------------------------------

def _circle_boundary_vectors(half_angle_deg: float, boresight: tuple[float, float, float],
                             n_pts: int = 4) -> list[tuple[float, float, float]]:
    ha = math.radians(half_angle_deg)
    # Construct orthonormal basis with boresight as Z
    bx, by, bz = boresight
    # Pick an arbitrary vector not parallel to boresight
    if abs(bx) < 0.9:
        ref = (1.0, 0.0, 0.0)
    else:
        ref = (0.0, 1.0, 0.0)
    # cross product boresight x ref -> u
    ux = by * ref[2] - bz * ref[1]
    uy = bz * ref[0] - bx * ref[2]
    uz = bx * ref[1] - by * ref[0]
    norm = math.sqrt(ux**2 + uy**2 + uz**2)
    ux, uy, uz = ux / norm, uy / norm, uz / norm
    # v = boresight x u
    vx = by * uz - bz * uy
    vy = bz * ux - bx * uz
    vz = bx * uy - by * ux

    vectors = []
    for i in range(n_pts):
        theta = 2 * math.pi * i / n_pts
        dx = math.sin(ha) * (math.cos(theta) * ux + math.sin(theta) * vx) + math.cos(ha) * bx
        dy = math.sin(ha) * (math.cos(theta) * uy + math.sin(theta) * vy) + math.cos(ha) * by
        dz = math.sin(ha) * (math.cos(theta) * uz + math.sin(theta) * vz) + math.cos(ha) * bz
        vectors.append((dx, dy, dz))
    return vectors


def _rect_boundary_vectors(ha_along: float, ha_cross: float,
                           boresight: tuple[float, float, float]) -> list[tuple[float, float, float]]:
    a = math.radians(ha_along)
    c = math.radians(ha_cross)
    bx, by, bz = boresight
    corners = [
        (math.tan(a), math.tan(c), 1.0),
        (-math.tan(a), math.tan(c), 1.0),
        (-math.tan(a), -math.tan(c), 1.0),
        (math.tan(a), -math.tan(c), 1.0),
    ]
    result = []
    for cx, cy, cz in corners:
        norm = math.sqrt(cx**2 + cy**2 + cz**2)
        result.append((cx / norm, cy / norm, cz / norm))
    return result


def generate_ik(instrument_name: str, fov_shape: str, fov_half_angle_deg: float,
                boresight: tuple[float, float, float], sc_id: int,
                naif_id_offset: int = 100,
                fov_half_angle_cross_deg: float | None = None) -> str:
    inst_id = sc_id - naif_id_offset

    if fov_shape.upper() == "CIRCLE":
        boundary = _circle_boundary_vectors(fov_half_angle_deg, boresight, 4)
        shape_str = "CIRCLE"
    elif fov_shape.upper() == "RECTANGLE":
        cross = fov_half_angle_cross_deg or fov_half_angle_deg
        boundary = _rect_boundary_vectors(fov_half_angle_deg, cross, boresight)
        shape_str = "RECTANGLE"
    else:
        boundary = _circle_boundary_vectors(fov_half_angle_deg, boresight, 4)
        shape_str = "CIRCLE"

    lines = [
        "KPL/IK",
        "",
        "\\begintext",
        f"  Instrument kernel for {instrument_name}",
        f"  NAIF ID: {inst_id}",
        f"  FOV: {shape_str} {fov_half_angle_deg} deg half-angle",
        "",
        "\\begindata",
        "",
        f"  INS{inst_id}_FOV_SHAPE        = '{shape_str}'",
        f"  INS{inst_id}_FOV_FRAME        = '{instrument_name}'",
        f"  INS{inst_id}_BORESIGHT        = ( {boresight[0]:.6f}, {boresight[1]:.6f}, {boresight[2]:.6f} )",
        f"  INS{inst_id}_FOV_CLASS_SPEC   = 'ANGLES'",
        f"  INS{inst_id}_FOV_REF_ANGLE    = {fov_half_angle_deg}",
        f"  INS{inst_id}_FOV_ANGLE_UNITS  = 'DEGREES'",
        "",
    ]

    # Boundary vectors
    ref_vec = boundary[0] if boundary else (1.0, 0.0, 0.0)
    lines.append(f"  INS{inst_id}_FOV_REF_VECTOR   = ( {ref_vec[0]:.6f}, {ref_vec[1]:.6f}, {ref_vec[2]:.6f} )")

    if shape_str == "RECTANGLE" and fov_half_angle_cross_deg is not None:
        lines.append(f"  INS{inst_id}_FOV_CROSS_ANGLE  = {fov_half_angle_cross_deg}")

    lines.append("")
    return "\n".join(lines)


