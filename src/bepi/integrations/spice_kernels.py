"""NAIF SPICE kernel generation — creates text kernel files from BEPI mission parameters.

Generates FK, IK, SCLK, SPK (mkspk input), CK (msopck input), PCK, MK, and DSK (mkdsk input)
kernel files for spacecraft missions without requiring spiceypy or SPICE toolkit at generation time.
"""
from dataclasses import dataclass, field
from datetime import datetime, timedelta
import math
import textwrap


# ---------------------------------------------------------------------------
# Dataclasses
# ---------------------------------------------------------------------------

@dataclass
class InstrumentDef:
    name: str
    abbrev: str  # e.g. "CAM1"
    naif_id_offset: int  # added to sc_id, e.g. -100 -> -100100
    fov_shape: str = "CIRCLE"  # CIRCLE | RECTANGLE | POLYGON
    fov_half_angle_deg: float = 5.0
    fov_half_angle_cross_deg: float | None = None  # for RECTANGLE
    boresight: tuple[float, float, float] = (0.0, 0.0, 1.0)
    frame_angles_deg: tuple[float, float, float] = (0.0, 0.0, 0.0)  # relative to SC body
    frame_axes: tuple[int, int, int] = (1, 2, 3)


@dataclass
class BodyDef:
    name: str
    naif_id: int
    radii_km: tuple[float, float, float]  # equatorial_a, equatorial_b, polar
    pole_ra_deg: float = 0.0
    pole_dec_deg: float = 90.0
    pm_deg: float = 0.0
    pm_rate_deg_per_day: float = 0.0


@dataclass
class OrbitState:
    epoch: str  # UTC string "2027-06-01T12:00:00"
    x_km: float = 0.0
    y_km: float = 0.0
    z_km: float = 0.0
    vx_km_s: float = 0.0
    vy_km_s: float = 0.0
    vz_km_s: float = 0.0


@dataclass
class MissionKernelParams:
    mission_name: str = "BEPISAT"
    sc_naif_id: int = -100000
    sc_body_axes: str = "XYZ"  # body frame axis labels
    instruments: list[InstrumentDef] = field(default_factory=list)
    orbit_sma_km: float = 6928.14
    orbit_ecc: float = 0.001
    orbit_inc_deg: float = 97.6
    orbit_raan_deg: float = 0.0
    orbit_aop_deg: float = 0.0
    orbit_ta_deg: float = 0.0
    central_body: str = "EARTH"
    central_body_mu_km3s2: float = 398600.4418
    epoch: str = "2027-06-01T12:00:00"
    mission_start: str = "2027-06-01T00:00:00"
    attitude_mode: str = "NADIR"  # NADIR | INERTIAL | SUN_POINTING
    tick_rate_hz: float = 1.0
    extra_bodies: list[BodyDef] = field(default_factory=list)
    kernel_dir: str = "kernels"


