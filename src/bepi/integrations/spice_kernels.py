# ---------------------------------------------------------------------------

def generate_sclk(sc_id: int, mission_start: str, tick_rate_hz: float = 1.0) -> str:
    abs_id = abs(sc_id)

    lines = [
        "KPL/SCLK",
        "",
        "\\begintext",
        f"  Spacecraft clock kernel for NAIF ID {sc_id}",
        f"  Mission start: {mission_start}",
        f"  Tick rate: {tick_rate_hz} Hz",
        "",
        "\\begindata",
        "",
        f"  SCLK_KERNEL_ID           = ( @{mission_start} )",
        f"  SCLK_DATA_TYPE_{abs_id}  = ( 1 )",
        f"  SCLK01_TIME_SYSTEM_{abs_id} = ( 1 )",
        f"  SCLK01_N_FIELDS_{abs_id} = ( 2 )",
        f"  SCLK01_MODULI_{abs_id}   = ( 4294967296 {int(tick_rate_hz)} )",
        f"  SCLK01_OFFSETS_{abs_id}  = ( 0 0 )",
        f"  SCLK01_OUTPUT_DELIM_{abs_id} = ( 1 )",
        "",
        f"  SCLK_PARTITION_START_{abs_id} = ( 0.0000000000000E+00 )",
        f"  SCLK_PARTITION_END_{abs_id}   = ( 2.8147497671066E+14 )",
        "",
        f"  SCLK01_COEFFICIENTS_{abs_id} = (",
        f"    0.0000000000000E+00  @{mission_start}  {1.0 / tick_rate_hz:.16E}",
        f"  )",
        "",
    ]
    return "\n".join(lines)


# ---------------------------------------------------------------------------
# SPK — mkspk input (state table)
# ---------------------------------------------------------------------------

def _kepler_to_state(sma_km: float, ecc: float, inc_deg: float, raan_deg: float,
                     aop_deg: float, ta_deg: float, mu: float) -> tuple[
                         tuple[float, float, float], tuple[float, float, float]]:
    inc = math.radians(inc_deg)
    raan = math.radians(raan_deg)
    aop = math.radians(aop_deg)
    ta = math.radians(ta_deg)

    p = sma_km * (1 - ecc**2)
    r_mag = p / (1 + ecc * math.cos(ta))

    # Perifocal coordinates
    r_pf = (r_mag * math.cos(ta), r_mag * math.sin(ta), 0.0)
    v_coeff = math.sqrt(mu / p)
    v_pf = (-v_coeff * math.sin(ta), v_coeff * (ecc + math.cos(ta)), 0.0)

    # Rotation matrix perifocal -> inertial
    cos_o, sin_o = math.cos(raan), math.sin(raan)
    cos_w, sin_w = math.cos(aop), math.sin(aop)
    cos_i, sin_i = math.cos(inc), math.sin(inc)

    r11 = cos_o * cos_w - sin_o * sin_w * cos_i
    r12 = -cos_o * sin_w - sin_o * cos_w * cos_i
    r21 = sin_o * cos_w + cos_o * sin_w * cos_i
    r22 = -sin_o * sin_w + cos_o * cos_w * cos_i
    r31 = sin_w * sin_i
    r32 = cos_w * sin_i

    rx = r11 * r_pf[0] + r12 * r_pf[1]
    ry = r21 * r_pf[0] + r22 * r_pf[1]
    rz = r31 * r_pf[0] + r32 * r_pf[1]

    vx = r11 * v_pf[0] + r12 * v_pf[1]
    vy = r21 * v_pf[0] + r22 * v_pf[1]
    vz = r31 * v_pf[0] + r32 * v_pf[1]

    return (rx, ry, rz), (vx, vy, vz)


def generate_spk_comment(orbit_params: dict, epoch: str, sc_id: int = -100000,
                         central_body_id: int = 399, mu: float = 398600.4418,
                         n_points: int = 60) -> str:
    sma = orbit_params.get("sma_km", 6928.14)
    ecc = orbit_params.get("ecc", 0.001)
    inc = orbit_params.get("inc_deg", 97.6)
    raan = orbit_params.get("raan_deg", 0.0)
    aop = orbit_params.get("aop_deg", 0.0)

    period_s = 2 * math.pi * math.sqrt(sma**3 / mu)
    dt = period_s / n_points

    try:
        t0 = datetime.fromisoformat(epoch)
    except ValueError:
        t0 = datetime(2027, 6, 1, 12, 0, 0)

    header = [
        f"\\begincomment",
        f"  SPK input file for mkspk utility",
        f"  Object: {sc_id}  Center: {central_body_id}",
        f"  Epoch: {epoch}",
        f"  Orbit: SMA={sma} km, ECC={ecc}, INC={inc} deg",
        f"  Period: {period_s:.1f} s ({period_s/60:.1f} min)",
        f"  Reference frame: J2000",
        f"\\endcomment",
        "",
        "\\begindata",
        "",
        f"  INPUT_DATA_TYPE  = 'STATES'",
        f"  OUTPUT_SPK_TYPE  = 5",
        f"  OBJECT_ID        = {sc_id}",
        f"  CENTER_ID        = {central_body_id}",
        f"  REF_FRAME_NAME   = 'J2000'",
        f"  PRODUCER_ID      = 'BEPI'",
        f"  DATA_ORDER       = 'EPOCH X Y Z VX VY VZ'",
        f"  TIME_WRAPPER     = '# ETSECONDS'",
        f"  DATA_DELIMITER   = ','",
        f"  LINES_PER_RECORD = 1",
        f"  INPUT_DATA_UNITS = ( 'ANGLES = DEGREES' 'DISTANCES = km' )",
        "",
        "\\begintext",
        "",
    ]

    states = []
    for i in range(n_points + 1):
        ta_deg = (orbit_params.get("ta_deg", 0.0) + (360.0 * i / n_points)) % 360.0
        pos, vel = _kepler_to_state(sma, ecc, inc, raan, aop, ta_deg, mu)
        t = t0 + timedelta(seconds=dt * i)
        et = dt * i  # seconds past epoch
        states.append(f"  {et:.3f}, {pos[0]:.6f}, {pos[1]:.6f}, {pos[2]:.6f}, "
                      f"{vel[0]:.6f}, {vel[1]:.6f}, {vel[2]:.6f}")

    return "\n".join(header + states) + "\n"


# ---------------------------------------------------------------------------
# CK — msopck input (attitude)
# ---------------------------------------------------------------------------

def generate_ck_comment(attitude_mode: str, sc_id: int, mission_name: str = "BEPISAT",
                        epoch: str = "2027-06-01T12:00:00") -> str:
    frame_id = sc_id * 1000

    mode_desc = {
        "NADIR": "Nadir-pointing: +Z toward Earth center, +X along velocity",
        "INERTIAL": "Inertial hold: fixed attitude in J2000",
        "SUN_POINTING": "Sun-pointing: +X toward Sun, +Z normal to orbit plane",
    }
    desc = mode_desc.get(attitude_mode.upper(), f"Custom mode: {attitude_mode}")

    # Quaternion for each mode (identity for inertial, placeholder for others)
    if attitude_mode.upper() == "INERTIAL":
        q = (0.0, 0.0, 0.0, 1.0)
    elif attitude_mode.upper() == "NADIR":
        q = (0.0, 0.7071068, 0.0, 0.7071068)  # 90 deg about Y
    else:
        q = (0.7071068, 0.0, 0.0, 0.7071068)  # 90 deg about X

    lines = [
        f"\\begincomment",
        f"  CK input file for msopck utility",
        f"  Spacecraft: {mission_name} (NAIF ID {sc_id})",
        f"  Frame ID: {frame_id}",
        f"  Attitude mode: {desc}",
        f"  Epoch: {epoch}",
        f"\\endcomment",
        "",
        "\\begindata",
        "",
        f"  CK_TYPE           = 3",
        f"  INSTRUMENT_ID     = {frame_id}",
        f"  REFERENCE_FRAME   = 'J2000'",
        f"  CK_SEGMENT_ID     = '{mission_name}_ATT'",
        f"  SCLK_KERNEL_FILE  = '{mission_name.lower()}.tsc'",
        f"  LSK_FILE_NAME     = 'naif0012.tls'",
        f"  PRODUCER_ID       = 'BEPI'",
        f"  INPUT_DATA_TYPE   = 'QUATERNIONS'",
        f"  INPUT_TIME_TYPE   = 'UTC'",
        f"  QUATERNION_TYPE   = 'SPICE'",
        "",
        "\\begintext",
        "",
        f"  UTC_TIME  Q0  Q1  Q2  Q3  AV1  AV2  AV3",
    ]

    # Generate 10 sample attitude records (constant attitude)
    try:
        t0 = datetime.fromisoformat(epoch)
    except ValueError:
        t0 = datetime(2027, 6, 1, 12, 0, 0)

    for i in range(10):
        t = t0 + timedelta(minutes=i * 10)
        ts = t.strftime("%Y-%m-%dT%H:%M:%S")
        lines.append(f"  {ts}  {q[0]:.7f}  {q[1]:.7f}  {q[2]:.7f}  {q[3]:.7f}  0.0  0.0  0.0")

    lines.append("")
    return "\n".join(lines)


# ---------------------------------------------------------------------------
# PCK — Planetary Constants Kernel
# ---------------------------------------------------------------------------

def generate_pck(bodies: list[BodyDef] | None = None) -> str:
    if bodies is None:
        bodies = STANDARD_BODIES

    lines = [
        "KPL/PCK",
        "",
        "\\begintext",
        "  Planetary constants kernel generated by BEPI",
        f"  Generated: {datetime.utcnow().isoformat()}",
        "  Source: IAU 2015 report + DE440",
        "",
        "\\begindata",
        "",
    ]

    for b in bodies:
        lines += [
            f"  BODY{b.naif_id}_RADII     = ( {b.radii_km[0]:.4f}, {b.radii_km[1]:.4f}, {b.radii_km[2]:.4f} )",
            f"  BODY{b.naif_id}_POLE_RA   = ( {b.pole_ra_deg:.6f}   0.0   0.0 )",
            f"  BODY{b.naif_id}_POLE_DEC  = ( {b.pole_dec_deg:.6f}   0.0   0.0 )",
            f"  BODY{b.naif_id}_PM        = ( {b.pm_deg:.6f}   {b.pm_rate_deg_per_day:.10f}   0.0 )",
            "",
        ]

    return "\n".join(lines)


