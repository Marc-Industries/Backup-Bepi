# MK — Meta-Kernel
# ---------------------------------------------------------------------------

def generate_mk(mission_name: str, kernel_files: list[str],
                kernel_dir: str = "kernels") -> str:
    lines = [
        "KPL/MK",
        "",
        "\\begintext",
        f"  Meta-kernel for {mission_name}",
        f"  Generated by BEPI on {datetime.utcnow().isoformat()}",
        "",
        "\\begindata",
        "",
        f"  PATH_VALUES  = ( '{kernel_dir}' )",
        f"  PATH_SYMBOLS = ( 'KERNELS' )",
        "",
        "  KERNELS_TO_LOAD = (",
    ]

    for kf in kernel_files:
        lines.append(f"    '$KERNELS/{kf}'")

    lines += [
        "  )",
        "",
    ]

    return "\n".join(lines)


# ---------------------------------------------------------------------------
# DSK — mkdsk input
# ---------------------------------------------------------------------------

def generate_dsk_input(body_name: str, shape: str = "ELLIPSOID",
                       radii_km: tuple[float, float, float] | None = None,
                       naif_id: int = 399) -> str:
    if radii_km is None:
        for b in STANDARD_BODIES:
            if b.name.upper() == body_name.upper():
                radii_km = b.radii_km
                naif_id = b.naif_id
                break
        else:
            radii_km = (100.0, 100.0, 100.0)

    lines = [
        f"\\begincomment",
        f"  DSK input for mkdsk utility",
        f"  Body: {body_name} (NAIF ID {naif_id})",
        f"  Shape: {shape}",
        f"  Radii: {radii_km[0]:.4f} x {radii_km[1]:.4f} x {radii_km[2]:.4f} km",
        f"\\endcomment",
        "",
        "\\begindata",
        "",
        f"  INPUT_SHAPE    = '{shape.upper()}'",
        f"  SURFACE_NAME   = '{body_name}_SURFACE'",
        f"  CENTER_NAME    = '{body_name}'",
        f"  REF_FRAME_NAME = 'IAU_{body_name.upper()}'",
        f"  START_TIME     = '2000-JAN-01'",
        f"  STOP_TIME      = '2100-JAN-01'",
        f"  DATA_CLASS     = 2",
        "",
    ]

    if shape.upper() == "ELLIPSOID":
        lines += [
            f"  A_RADIUS       = {radii_km[0]:.4f}",
            f"  B_RADIUS       = {radii_km[1]:.4f}",
            f"  C_RADIUS       = {radii_km[2]:.4f}",
            "",
        ]
    elif shape.upper() == "SPHERE":
        r = radii_km[0]
        lines += [
            f"  A_RADIUS       = {r:.4f}",
            f"  B_RADIUS       = {r:.4f}",
            f"  C_RADIUS       = {r:.4f}",
            "",
        ]

    return "\n".join(lines)


# ---------------------------------------------------------------------------
# generate_all_kernels
# ---------------------------------------------------------------------------

def generate_all_kernels(params: MissionKernelParams) -> dict[str, str]:
    name = params.mission_name
    sc_id = params.sc_naif_id
    kernels: dict[str, str] = {}

    # FK
    fk_file = f"{name.lower()}.tf"
    kernels[fk_file] = generate_fk(name, sc_id, params.sc_body_axes, params.instruments)

    # IK for each instrument
    for inst in params.instruments:
        ik_file = f"{name.lower()}_{inst.abbrev.lower()}.ti"
        kernels[ik_file] = generate_ik(
            f"{name}_{inst.abbrev}", inst.fov_shape, inst.fov_half_angle_deg,
            inst.boresight, sc_id, inst.naif_id_offset, inst.fov_half_angle_cross_deg,
        )

    # SCLK
    sclk_file = f"{name.lower()}.tsc"
    kernels[sclk_file] = generate_sclk(sc_id, params.mission_start, params.tick_rate_hz)

    # SPK (mkspk input)
    spk_file = f"{name.lower()}_spk.txt"
    orbit_dict = {
        "sma_km": params.orbit_sma_km,
        "ecc": params.orbit_ecc,
        "inc_deg": params.orbit_inc_deg,
        "raan_deg": params.orbit_raan_deg,
        "aop_deg": params.orbit_aop_deg,
        "ta_deg": params.orbit_ta_deg,
    }
    central_ids = {"EARTH": 399, "MOON": 301, "MARS": 499, "JUPITER": 599}
    center_id = central_ids.get(params.central_body.upper(), 399)
    kernels[spk_file] = generate_spk_comment(
        orbit_dict, params.epoch, sc_id, center_id, params.central_body_mu_km3s2,
    )

    # CK (msopck input)
    ck_file = f"{name.lower()}_ck.txt"
    kernels[ck_file] = generate_ck_comment(params.attitude_mode, sc_id, name, params.epoch)

    # PCK
    all_bodies = list(STANDARD_BODIES) + list(params.extra_bodies)
    pck_file = f"{name.lower()}.tpc"
    kernels[pck_file] = generate_pck(all_bodies)

    # DSK input for central body
    dsk_file = f"{name.lower()}_{params.central_body.lower()}_dsk.txt"
    kernels[dsk_file] = generate_dsk_input(params.central_body)

    # MK (meta-kernel) — references the generated text kernels
    loadable = [fk_file, sclk_file, pck_file]
    for inst in params.instruments:
        loadable.append(f"{name.lower()}_{inst.abbrev.lower()}.ti")
    mk_file = f"{name.lower()}.tm"
    kernels[mk_file] = generate_mk(name, loadable, params.kernel_dir)

    return kernels
