def generate_total_mission_freeflyer(profile: MissionProfile, sc: SpacecraftParams | None = None) -> str:
    sc = sc or SpacecraftParams()
    lines = [
        f"// FreeFlyer — Total Mission Script: {profile.name}",
        "// Auto-generated by BEPI",
        "",
        f"Spacecraft {sc.name};",
        f"{sc.name}.DryMass = {sc.dry_mass_kg};",
        f"{sc.name}.Cd = {sc.cd};",
        f"{sc.name}.Cr = {sc.cr};",
        "",
    ]

    for i, phase in enumerate(profile.phases):
        tag = f"Phase{i}"
        orb = _phase_orbit(phase)
        body = phase.body
        dur_s = phase.duration_days * 86400

        if _is_helio(phase.phase_type):
            body = "Sun"
            b_dep = get_body(phase.body)
            orb = OrbitParams(
                sma_km=b_dep.sma_au * _AU_KM,
                ecc=0.0,
                inc_deg=0.0,
                body="Sun",
            )

        lines += [
            f"// --- {phase.name} ({phase.phase_type.value}) around {body} ---",
            f"{sc.name}.CentralBody = \"{body}\";",
            f"{sc.name}.A = {orb.sma_km:.4f};",
            f"{sc.name}.E = {orb.ecc};",
            f"{sc.name}.I = {orb.inc_deg};",
            f"{sc.name}.RAAN = {orb.raan_deg};",
            f"{sc.name}.W = {orb.aop_deg};",
            f"{sc.name}.TA = {orb.ta_deg};",
            "",
        ]

        if phase.delta_v_ms > 0:
            dv_kms = phase.delta_v_ms / 1000.0
            lines += [
                f"ImpulsiveBurn burn_{tag};",
                f"burn_{tag}.DeltaV = [{dv_kms:.6f}, 0, 0];  // km/s",
                f"{sc.name}.Maneuver(burn_{tag});",
                "",
            ]

        lines += [
            f"While ({sc.name}.ElapsedTime < {dur_s:.0f});",
            f"    Step {sc.name};",
            f"End;",
            "",
        ]

    lines.append(f"// End of mission: {profile.name}")
    return "\n".join(lines)


