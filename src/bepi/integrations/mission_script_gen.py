"""Total Mission script generation — single script spanning all mission phases.

Generates unified GMAT, FreeFlyer, and MATLAB scripts that cover the complete
mission profile: departure orbit, escape, heliocentric transfer, capture,
destination orbit — with body transitions and ΔV annotations.
"""
from __future__ import annotations

import math
from .celestial_bodies import get_body, BODIES
from .mission_phases import MissionProfile, MissionPhase, PhaseType
from .gmat import SpacecraftParams, OrbitParams, PropagationConfig


_AU_KM = 1.496e8


def _phase_orbit(phase: MissionPhase) -> OrbitParams:
    fp = phase.full_orbital_params
    b = get_body(phase.body)
    alt = fp.get("alt_km", 550)
    return OrbitParams(
        sma_km=b.radius_km + alt,
        ecc=fp.get("ecc", 0.001),
        inc_deg=fp.get("inc_deg", 0.0),
        raan_deg=fp.get("raan_deg", 0.0),
        aop_deg=fp.get("aop_deg", 0.0),
        ta_deg=0.0,
        body=phase.body,
    )


def _is_helio(pt: PhaseType) -> bool:
    return pt in (PhaseType.TRANSFER, PhaseType.ESCAPE, PhaseType.FLYBY)


# ---------------------------------------------------------------------------
# GMAT
# ---------------------------------------------------------------------------

def generate_total_mission_gmat(profile: MissionProfile, sc: SpacecraftParams | None = None) -> str:
    sc = sc or SpacecraftParams()
    lines = [
        f"%% GMAT — Total Mission Script: {profile.name}",
        "%% Auto-generated by BEPI — contains all mission phases",
        "%% WARNING: this is a preliminary script; verify force models before use",
        "",
    ]

    # Spacecraft
    lines += [
        f"Create Spacecraft {sc.name};",
        f"{sc.name}.DryMass = {sc.dry_mass_kg};",
        f"{sc.name}.Cd = {sc.cd};",
        f"{sc.name}.Cr = {sc.cr};",
        f"{sc.name}.DragArea = {sc.drag_area_m2};",
        f"{sc.name}.SRPArea = {sc.srp_area_m2};",
        "",
    ]

    # Per-phase blocks
    for i, phase in enumerate(profile.phases):
        tag = f"Phase{i}"
        orb = _phase_orbit(phase)
        body = phase.body
        gmat_body = "Luna" if body == "Moon" else body
        coord = f"{gmat_body}MJ2000Eq"

        if _is_helio(phase.phase_type):
            body = "Sun"
            gmat_body = "Sun"
            coord = "SunMJ2000Eq"
            b_dep = get_body(phase.body)
            orb = OrbitParams(
                sma_km=b_dep.sma_au * _AU_KM,
                ecc=0.0,
                inc_deg=0.0,
                body="Sun",
            )

        dur_s = phase.duration_days * 86400

        lines += [
            f"%% --- {phase.name} ({phase.phase_type.value}) around {body} ---",
            f"Create ForceModel fm_{tag};",
            f"fm_{tag}.CentralBody = {gmat_body};",
            f"fm_{tag}.PointMasses = {{{gmat_body}}};",
            "",
            f"Create Propagator prop_{tag};",
            f"prop_{tag}.FM = fm_{tag};",
            f"prop_{tag}.Type = RungeKutta89;",
            f"prop_{tag}.InitialStepSize = 60;",
            "",
            f"{sc.name}.CoordinateSystem = {coord};",
            f"{sc.name}.SMA = {orb.sma_km:.4f};",
            f"{sc.name}.ECC = {orb.ecc};",
            f"{sc.name}.INC = {orb.inc_deg};",
            f"{sc.name}.RAAN = {orb.raan_deg};",
            f"{sc.name}.AOP = {orb.aop_deg};",
            f"{sc.name}.TA = {orb.ta_deg};",
            "",
        ]

        if phase.delta_v_ms > 0:
            dv_kms = phase.delta_v_ms / 1000.0
            lines += [
                f"Create ImpulsiveBurn burn_{tag};",
                f"burn_{tag}.CoordinateSystem = {coord};",
                f"burn_{tag}.Element1 = {dv_kms:.6f};  % km/s ({phase.delta_v_ms:.1f} m/s)",
                "",
                f"Maneuver burn_{tag}({sc.name});",
            ]

        lines += [
            f"Propagate prop_{tag}({sc.name}) {{{sc.name}.ElapsedSecs = {dur_s:.0f}}};",
            "",
        ]

    lines.append(f"%% End of mission: {profile.name}")
    return "\n".join(lines)


