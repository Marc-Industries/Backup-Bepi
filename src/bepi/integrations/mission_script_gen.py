def generate_total_mission_matlab(profile: MissionProfile, sc: SpacecraftParams | None = None) -> str:
    sc = sc or SpacecraftParams()
    lines = [
        f"%% BEPI — Total Mission MATLAB Script: {profile.name}",
        "%% Auto-generated — requires Aerospace Toolbox",
        "",
        f"m_dry = {sc.dry_mass_kg};  % kg",
        f"Cd = {sc.cd};",
        "Isp = 220;  % s (hydrazine default)",
        "g0 = 9.80665;  % m/s^2",
        "total_dv = 0;  % m/s cumulative",
        "",
    ]

    for i, phase in enumerate(profile.phases):
        tag = f"phase{i}"
        body = phase.body
        b = get_body(body)
        orb = _phase_orbit(phase)
        dur_s = phase.duration_days * 86400

        if _is_helio(phase.phase_type):
            body = "Sun"
            b = get_body("Sun")
            b_dep = get_body(phase.body)
            sma = b_dep.sma_au * _AU_KM
        else:
            sma = orb.sma_km

        period_s = 2 * math.pi * (sma ** 3 / b.mu_km3s2) ** 0.5

        lines += [
            f"%% --- {phase.name} ({phase.phase_type.value}) around {body} ---",
            f"mu_{tag} = {b.mu_km3s2:.4f};  % km^3/s^2",
            f"sma_{tag} = {sma:.1f};  % km",
            f"T_{tag} = 2*pi*sqrt(sma_{tag}^3/mu_{tag});  % period: {period_s/60:.1f} min",
            f"dur_{tag} = {dur_s:.0f};  % s ({phase.duration_days:.1f} days)",
            "",
        ]

        if phase.delta_v_ms > 0:
            dv = phase.delta_v_ms
            lines += [
                f"dv_{tag} = {dv:.1f};  % m/s",
                f"total_dv = total_dv + dv_{tag};",
                f"fprintf('Phase {i} ({phase.name}): dV = %.1f m/s\\n', dv_{tag});",
                "",
            ]

        lines += [
            f"n_orbits_{tag} = ceil(dur_{tag} / T_{tag});",
            f"fprintf('{phase.name}: %d orbits over %.1f days around {body}\\n', n_orbits_{tag}, dur_{tag}/86400);",
            "",
        ]

    lines += [
        "%% --- Mission Summary ---",
        "fprintf('\\n=== TOTAL MISSION SUMMARY ===\\n');",
        f"fprintf('Mission: {profile.name}\\n');",
        f"fprintf('Phases: {len(profile.phases)}\\n');",
        "fprintf('Total delta-V: %.1f m/s\\n', total_dv);",
        "",
        "%% Propellant estimate (Tsiolkovsky)",
        "mass_ratio = exp(total_dv / (Isp * g0));",
        "m_prop = m_dry * (mass_ratio - 1);",
        "fprintf('Estimated propellant: %.2f kg (Isp=%d s)\\n', m_prop, Isp);",
    ]

    return "\n".join(lines)
