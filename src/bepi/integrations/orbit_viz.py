def plot_single_orbit(altitude_km, inclination_deg, raan_deg=0, ecc=0, aop_deg=0, n_points=360, body="Earth"):
    b = get_body(body)
    a = b.radius_km + altitude_km
    xs, ys, zs = _keplerian_orbit(a, ecc, inclination_deg, raan_deg, aop_deg, n_points)

    fig = go.Figure()
    for t in _body_traces(body):
        fig.add_trace(t)

    fig.add_trace(go.Scatter3d(
        x=xs, y=ys, z=zs, mode="lines",
        line=dict(color="cyan", width=3),
        name="Orbit",
    ))

    # Ground track projection
    norms = np.sqrt(xs**2 + ys**2 + zs**2)
    gx = xs / norms * R_EARTH
    gy = ys / norms * R_EARTH
    gz = zs / norms * R_EARTH
    fig.add_trace(go.Scatter3d(
        x=gx, y=gy, z=gz, mode="lines",
        line=dict(color="yellow", width=1, dash="dot"),
        name="Ground Track", opacity=0.5,
    ))

    # Ascending node (orbit crosses z=0 going up)
    for i in range(1, len(zs)):
        if zs[i - 1] <= 0 < zs[i]:
            fig.add_trace(go.Scatter3d(
                x=[xs[i]], y=[ys[i]], z=[zs[i]], mode="markers",
                marker=dict(size=6, color="lime", symbol="diamond"),
                name="Ascending Node",
            ))
            break
    for i in range(1, len(zs)):
        if zs[i - 1] >= 0 > zs[i]:
            fig.add_trace(go.Scatter3d(
                x=[xs[i]], y=[ys[i]], z=[zs[i]], mode="markers",
                marker=dict(size=6, color="red", symbol="diamond"),
                name="Descending Node",
            ))
            break

    # Orbital plane (faint)
    theta = np.linspace(0, 2 * np.pi, 60)
    r_plane = a * 1.1
    inc = math.radians(inclination_deg)
    raan = math.radians(raan_deg)
    px = r_plane * np.cos(theta)
    py = r_plane * np.sin(theta)
    # Rotate to orbital plane
    rx = px * math.cos(raan) - py * math.sin(raan) * math.cos(inc)
    ry = px * math.sin(raan) + py * math.cos(raan) * math.cos(inc)
    rz = py * math.sin(inc)
    fig.add_trace(go.Mesh3d(
        x=np.concatenate([[0], rx]),
        y=np.concatenate([[0], ry]),
        z=np.concatenate([[0], rz]),
        opacity=0.05, color="cyan",
        showlegend=False, hoverinfo="skip",
    ))

    fig.update_layout(
        title=f"{body} Orbit: {altitude_km:.0f} km, {inclination_deg:.1f}° inc, e={ecc:.4f}",
        **_scene_layout(a * 1.5),
    )
    return fig


