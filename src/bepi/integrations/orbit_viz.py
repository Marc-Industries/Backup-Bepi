def plot_ground_track(altitude_km, inclination_deg, raan_deg=0, n_orbits=3, body="Earth",
                      ground_stations=None, swath_half_angle_deg=0):
    b = get_body(body)
    R = b.radius_km
    mu = b.mu_km3s2
    a = R + altitude_km
    T = 2 * math.pi * math.sqrt(a**3 / mu)
    omega_body = 2 * math.pi / b.rotation_period_s

    inc = math.radians(inclination_deg)
    raan0 = math.radians(raan_deg)

    n_pts = 500 * n_orbits
    t = np.linspace(0, T * n_orbits, n_pts)
    nu = 2 * np.pi * t / T

    j2 = b.j2
    raan_dot = -1.5 * j2 * (R / a)**2 * math.cos(inc) * (2 * math.pi / T) if j2 > 0 else 0.0
    raan_t = raan0 + raan_dot * t

    cos_i = math.cos(inc)
    sin_i = math.sin(inc)
    cos_r = np.cos(raan_t)
    sin_r = np.sin(raan_t)
    cos_nu = np.cos(nu)
    sin_nu = np.sin(nu)

    x_eci = a * (cos_r * cos_nu - sin_r * sin_nu * cos_i)
    y_eci = a * (sin_r * cos_nu + cos_r * sin_nu * cos_i)
    z_eci = a * sin_nu * sin_i

    gst = omega_body * t
    x_ecef = x_eci * np.cos(gst) + y_eci * np.sin(gst)
    y_ecef = -x_eci * np.sin(gst) + y_eci * np.cos(gst)

    lat = np.degrees(np.arcsin(np.clip(z_eci / a, -1, 1)))
    lon = np.degrees(np.arctan2(y_ecef, x_ecef))

    # Split track into contiguous ascending/descending arcs
    d_lat = np.diff(lat, prepend=lat[0])
    asc_segs, desc_segs = [], []
    cur_lon, cur_lat = [lon[0]], [lat[0]]
    cur_asc = d_lat[0] >= 0
    for i in range(1, len(lon)):
        is_asc = d_lat[i] >= 0
        # Longitude wrap break
        lon_jump = abs(lon[i] - lon[i - 1]) > 300
        if is_asc != cur_asc or lon_jump:
            if len(cur_lon) > 1:
                (asc_segs if cur_asc else desc_segs).append((cur_lon, cur_lat))
            cur_lon, cur_lat = [lon[i - 1], lon[i]], [lat[i - 1], lat[i]]
            if lon_jump:
                cur_lon, cur_lat = [lon[i]], [lat[i]]
            cur_asc = is_asc
        else:
            cur_lon.append(lon[i])
            cur_lat.append(lat[i])
    if len(cur_lon) > 1:
        (asc_segs if cur_asc else desc_segs).append((cur_lon, cur_lat))

    # Flatten into None-separated lists
    def _flatten_segs(segs):
        out_lon, out_lat = [], []
        for slon, slat in segs:
            if out_lon:
                out_lon.append(None)
                out_lat.append(None)
            out_lon.extend(slon)
            out_lat.extend(slat)
        return out_lon, out_lat

    asc_lon, asc_lat = _flatten_segs(asc_segs)
    desc_lon, desc_lat = _flatten_segs(desc_segs)

    period_min = T / 60
    body_label = f" ({body})" if body != "Earth" else ""
    title = f"Ground Track{body_label}: {altitude_km:.0f} km, {inclination_deg:.1f}° inc, {n_orbits} rev (T={period_min:.1f} min)"

    # --- Earth: use Scattergeo with built-in map ---
    if body == "Earth":
        return _ground_track_geo(
            asc_lon, asc_lat, desc_lon, desc_lat, lon, lat,
            altitude_km, R, swath_half_angle_deg, ground_stations, title,
        )

    # --- Non-Earth: fallback with manual coastlines ---
    return _ground_track_flat(
        asc_lon, asc_lat, desc_lon, desc_lat, lon, lat,
        altitude_km, R, swath_half_angle_deg, ground_stations, title, body,
    )


def _ground_track_geo(asc_lon, asc_lat, desc_lon, desc_lat, lon, lat,
                       altitude_km, R, swath_half_angle_deg, ground_stations, title):
    fig = go.Figure()

    # Split None-separated lists into segments for Scattergeo
    def _segments(lons, lats):
        segs = []
        cur_lon, cur_lat = [], []
        for lo, la in zip(lons, lats):
            if lo is None:
                if cur_lon:
                    segs.append((cur_lon, cur_lat))
                cur_lon, cur_lat = [], []
            else:
                cur_lon.append(lo)
                cur_lat.append(la)
        if cur_lon:
            segs.append((cur_lon, cur_lat))
        return segs

    for seg_lon, seg_lat in _segments(asc_lon, asc_lat):
        fig.add_trace(go.Scattergeo(
            lon=seg_lon, lat=seg_lat, mode="lines",
            line=dict(color="cyan", width=2), name="Ascending",
            showlegend=(seg_lon is _segments(asc_lon, asc_lat)[0][0]),
        ))
    # fix showlegend: only first ascending segment
    first_asc = True
    first_desc = True
    fig.data = []  # reset and redo properly
    for seg_lon, seg_lat in _segments(asc_lon, asc_lat):
        fig.add_trace(go.Scattergeo(
            lon=seg_lon, lat=seg_lat, mode="lines",
            line=dict(color="cyan", width=2), name="Ascending",
            showlegend=first_asc,
        ))
        first_asc = False
    for seg_lon, seg_lat in _segments(desc_lon, desc_lat):
        fig.add_trace(go.Scattergeo(
            lon=seg_lon, lat=seg_lat, mode="lines",
            line=dict(color="orange", width=2), name="Descending",
            showlegend=first_desc,
        ))
        first_desc = False

    # Swath overlay
    if swath_half_angle_deg > 0:
        swath_km = altitude_km * math.tan(math.radians(swath_half_angle_deg))
        swath_deg = math.degrees(swath_km / R)
        for arr_lon, arr_lat, clr in [(asc_lon, asc_lat, "rgba(0,255,255,0.15)"),
                                       (desc_lon, desc_lat, "rgba(255,165,0,0.15)")]:
            for seg_lon, seg_lat in _segments(arr_lon, arr_lat):
                upper = [la + swath_deg for la in seg_lat]
                lower = [la - swath_deg for la in seg_lat]
                fig.add_trace(go.Scattergeo(
                    lon=seg_lon + seg_lon[::-1],
                    lat=upper + lower[::-1],
                    fill="toself", fillcolor=clr,
                    line=dict(width=0), showlegend=False, hoverinfo="skip",
                ))

    # Start marker
    fig.add_trace(go.Scattergeo(
        lon=[lon[0]], lat=[lat[0]], mode="markers+text",
        marker=dict(size=8, color="lime"), text=["Start"],
        textposition="top center", textfont=dict(color="lime", size=10),
        name="Start", showlegend=False,
    ))

    # Ground stations
    if ground_stations:
        fig.add_trace(go.Scattergeo(
            lon=[gs[1] for gs in ground_stations],
            lat=[gs[2] for gs in ground_stations],
            mode="markers+text",
            marker=dict(size=8, color="red", symbol="triangle-up"),
            text=[gs[0] for gs in ground_stations],
            textposition="top center", textfont=dict(color="red", size=9),
            name="Ground Stations",
        ))

    fig.update_geos(
        projection_type="equirectangular",
        showland=True, landcolor="rgb(30,65,30)",
        showocean=True, oceancolor="rgb(8,25,70)",
        showcoastlines=True, coastlinecolor="rgb(60,160,90)", coastlinewidth=1,
        showlakes=True, lakecolor="rgb(8,25,70)",
        showcountries=True, countrycolor="rgba(80,80,120,0.4)", countrywidth=0.5,
        showrivers=True, rivercolor="rgba(30,80,160,0.3)", riverwidth=0.5,
        bgcolor="rgb(10,10,30)",
        lataxis=dict(range=[-90, 90], dtick=30, gridcolor="rgba(100,100,150,0.3)"),
        lonaxis=dict(range=[-180, 180], dtick=30, gridcolor="rgba(100,100,150,0.3)"),
    )
    fig.update_layout(
        title=dict(text=title, font=dict(color="white")),
        paper_bgcolor="rgb(10,10,30)",
        font=dict(color="white"),
        margin=dict(l=10, r=10, t=40, b=10),
        height=520,
        legend=dict(font=dict(color="white"), bgcolor="rgba(10,10,30,0.85)", bordercolor="rgba(100,100,150,0.5)", borderwidth=1),
    )
    return fig


