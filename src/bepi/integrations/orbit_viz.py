def plot_constellation(n_planes, sats_per_plane, altitude_km, inclination_deg,
                       phase_offset_deg=0, fov_half_angle_deg=0, body="Earth"):
    b = get_body(body)
    R = b.radius_km
    a = R + altitude_km
    fig = go.Figure()
    for t in _body_traces(body):
        fig.add_trace(t)

    colors = [
        "cyan", "lime", "magenta", "orange", "yellow",
        "deepskyblue", "coral", "mediumpurple", "springgreen", "tomato",
    ]

    # Precompute FOV ground footprint radius (Earth-central angle)
    fov_eca_deg = 0
    if fov_half_angle_deg > 0:
        eta = math.asin(R / a * math.sin(math.radians(fov_half_angle_deg + 90)))
        if not math.isnan(eta):
            eps = math.pi - math.radians(fov_half_angle_deg + 90) - eta
            fov_eca_deg = math.degrees(eps)

    sat_positions = []

    for p in range(n_planes):
        raan = 360 * p / n_planes
        color = colors[p % len(colors)]

        xs, ys, zs = _keplerian_orbit(a, 0, inclination_deg, raan, 0)
        fig.add_trace(go.Scatter3d(
            x=xs, y=ys, z=zs, mode="lines",
            line=dict(color=color, width=1.5), opacity=0.4,
            name=f"Plane {p + 1}", showlegend=(p < 8),
        ))

        phase_shift = phase_offset_deg * p
        inc = math.radians(inclination_deg)
        raan_r = math.radians(raan)
        cos_r, sin_r = math.cos(raan_r), math.sin(raan_r)
        cos_i, sin_i = math.cos(inc), math.sin(inc)

        for s in range(sats_per_plane):
            nu_deg = 360 * s / sats_per_plane + phase_shift
            nu = math.radians(nu_deg)
            xp = a * math.cos(nu)
            yp = a * math.sin(nu)
            sx = cos_r * xp - sin_r * yp * cos_i
            sy = sin_r * xp + cos_r * yp * cos_i
            sz = yp * sin_i

            fig.add_trace(go.Scatter3d(
                x=[sx], y=[sy], z=[sz], mode="markers",
                marker=dict(size=3, color=color),
                showlegend=False, hovertext=f"P{p + 1}-S{s + 1}",
                hoverinfo="text",
            ))
            sat_positions.append((sx, sy, sz, color))

    # FOV cones projected onto body surface
    if fov_eca_deg > 0 and len(sat_positions) <= 100:
        n_circle = 36
        circle_angles = np.linspace(0, 2 * np.pi, n_circle)
        eca_r = math.radians(fov_eca_deg)
        for sx, sy, sz, color in sat_positions:
            sr = math.sqrt(sx**2 + sy**2 + sz**2) or 1.0
            slat = math.asin(sz / sr)
            slon = math.atan2(sy, sx)
            flon, flat, fz = [], [], []
            for ca in circle_angles:
                lat2 = math.asin(math.sin(slat) * math.cos(eca_r) +
                                 math.cos(slat) * math.sin(eca_r) * math.cos(ca))
                dlon = math.atan2(math.sin(ca) * math.sin(eca_r) * math.cos(slat),
                                  math.cos(eca_r) - math.sin(slat) * math.sin(lat2))
                lon2 = slon + dlon
                fx = R * 1.005 * math.cos(lat2) * math.cos(lon2)
                fy = R * 1.005 * math.cos(lat2) * math.sin(lon2)
                fzz = R * 1.005 * math.sin(lat2)
                flon.append(fx)
                flat.append(fy)
                fz.append(fzz)
            flon.append(flon[0])
            flat.append(flat[0])
            fz.append(fz[0])
            fig.add_trace(go.Scatter3d(
                x=flon, y=flat, z=fz, mode="lines",
                line=dict(color=color, width=1), opacity=0.5,
                showlegend=False, hoverinfo="skip",
            ))

    total = n_planes * sats_per_plane
    T_orb = 2 * math.pi * math.sqrt(a**3 / b.mu_km3s2)
    max_lat = min(inclination_deg, 180 - inclination_deg)

    fig.update_layout(
        title=f"Walker {total}/{n_planes}/{int(phase_offset_deg) if phase_offset_deg else 0}: "
              f"{altitude_km:.0f} km, {inclination_deg:.1f}° inc",
        **_scene_layout(a * 1.6),
    )

    info = {
        "total_sats": total, "n_planes": n_planes, "sats_per_plane": sats_per_plane,
        "altitude_km": altitude_km, "period_min": T_orb / 60,
        "max_latitude_deg": max_lat,
        "fov_ground_radius_km": fov_eca_deg * math.pi / 180 * R if fov_eca_deg > 0 else 0,
        "fov_ground_radius_deg": fov_eca_deg,
        "walker_notation": f"{total}/{n_planes}/{int(phase_offset_deg) if phase_offset_deg else 0}",
    }
    return fig, info


