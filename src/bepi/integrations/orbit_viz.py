def _lonlat_to_xyz(lon_deg, lat_deg, r=R_EARTH):
    lon = math.radians(lon_deg)
    lat = math.radians(lat_deg)
    x = r * math.cos(lat) * math.cos(lon)
    y = r * math.cos(lat) * math.sin(lon)
    z = r * math.sin(lat)
    return x, y, z


# NASA Blue Marble texture (180×360×3 RGB), zlib-compressed
_EARTH_TEX_PATH = pathlib.Path(__file__).parent / "earth_texture.bin"
_EARTH_TEX_RGB = None
_EARTH_SPHERE_CACHE = None


def _load_earth_texture():
    global _EARTH_TEX_RGB
    if _EARTH_TEX_RGB is not None:
        return _EARTH_TEX_RGB
    raw = zlib.decompress(_EARTH_TEX_PATH.read_bytes())
    _EARTH_TEX_RGB = np.frombuffer(raw, dtype=np.uint8).reshape(180, 360, 3)
    return _EARTH_TEX_RGB


def _make_earth_sphere():
    global _EARTH_SPHERE_CACHE
    if _EARTH_SPHERE_CACHE is not None:
        return _EARTH_SPHERE_CACHE

    tex = _load_earth_texture()  # 180×360×3
    n_lon, n_lat = 200, 100
    u = np.linspace(0, 2 * np.pi, n_lon, endpoint=False)
    v = np.linspace(0, np.pi, n_lat)

    x = R_EARTH * np.outer(np.cos(u), np.sin(v))
    y = R_EARTH * np.outer(np.sin(u), np.sin(v))
    z = R_EARTH * np.outer(np.ones(n_lon), np.cos(v))

    lon_idx = np.clip((np.degrees(u) / 360 * 360).astype(int), 0, 359)
    lat_idx = np.clip((np.degrees(v) / 180 * 180).astype(int), 0, 179)

    # Sample texture into vertex arrays using broadcasting
    tex_sampled = tex[np.ix_(lat_idx, lon_idx)]  # (n_lat, n_lon, 3)
    vertex_r = tex_sampled[:, :, 0].T.astype(np.float64)  # (n_lon, n_lat)
    vertex_g = tex_sampled[:, :, 1].T.astype(np.float64)
    vertex_b = tex_sampled[:, :, 2].T.astype(np.float64)

    # Build N evenly-spaced palette entries from all vertex colors, sorted by luminance
    N = 256
    flat_r = vertex_r.ravel()
    flat_g = vertex_g.ravel()
    flat_b = vertex_b.ravel()
    lum = 0.299 * flat_r + 0.587 * flat_g + 0.114 * flat_b
    order = np.argsort(lum)
    step = max(len(order) // N, 1)
    palette_idx = order[::step][:N]
    palette = np.stack([flat_r[palette_idx], flat_g[palette_idx], flat_b[palette_idx]], axis=1)

    # For each vertex find nearest palette entry (Euclidean in RGB)
    rgb_flat = np.stack([flat_r, flat_g, flat_b], axis=1)  # (n_lon*n_lat, 3)
    # Vectorized nearest: expand dims for broadcasting
    dists = np.linalg.norm(rgb_flat[:, None, :] - palette[None, :, :], axis=2)  # (P, N)
    nearest = np.argmin(dists, axis=1)  # (P,)
    surfacecolor = (nearest / max(N - 1, 1)).reshape(n_lon, n_lat)

    colorscale = []
    for i in range(N):
        norm = i / max(N - 1, 1)
        r, g, b = int(palette[i, 0]), int(palette[i, 1]), int(palette[i, 2])
        colorscale.append([norm, f"rgb({r},{g},{b})"])

    surf = go.Surface(
        x=x, y=y, z=z,
        surfacecolor=surfacecolor,
        colorscale=colorscale,
        showscale=False,
        opacity=1.0,
        hoverinfo="skip",
        name="Earth",
        lighting=dict(ambient=0.65, diffuse=0.6, specular=0.1, roughness=0.9, fresnel=0.05),
        lightposition=dict(x=10000, y=10000, z=10000),
    )
    _EARTH_SPHERE_CACHE = surf
    return surf


def _make_coastlines_3d(r=R_EARTH * 1.005):
    traces = []
    for name, coords in _COASTLINES.items():
        # Split at None separators
        segments = []
        current = []
        for c in coords:
            if c is None:
                if current:
                    segments.append(current)
                current = []
            else:
                current.append(c)
        if current:
            segments.append(current)

        for seg in segments:
            xs, ys, zs = [], [], []
            for lon, lat in seg:
                x, y, z = _lonlat_to_xyz(lon, lat, r)
                xs.append(x)
                ys.append(y)
                zs.append(z)
            traces.append(go.Scatter3d(
                x=xs, y=ys, z=zs,
                mode="lines", line=dict(color="rgba(200,220,200,0.6)", width=1),
                showlegend=False, hoverinfo="skip", name=name,
            ))
    return traces


