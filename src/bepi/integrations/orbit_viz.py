_PLANET_DATA = {
    "Mercury": {"r_au": 0.387, "color": "gray", "size": 4},
    "Venus":   {"r_au": 0.723, "color": "orange", "size": 6},
    "Earth":   {"r_au": 1.000, "color": "deepskyblue", "size": 6},
    "Mars":    {"r_au": 1.524, "color": "orangered", "size": 5},
    "Jupiter": {"r_au": 5.203, "color": "sandybrown", "size": 10},
    "Saturn":  {"r_au": 9.537, "color": "gold", "size": 9},
}


def interplanetary_data(departure_planet="Earth", arrival_planet="Mars"):
    dep = _PLANET_DATA[departure_planet]
    arr = _PLANET_DATA[arrival_planet]
    r1_au = dep["r_au"]
    r2_au = arr["r_au"]
    r1_km = r1_au * AU_KM
    r2_km = r2_au * AU_KM
    a_t_km = (r1_km + r2_km) / 2

    # Transfer time (Hohmann half-period)
    tof_s = math.pi * math.sqrt(a_t_km**3 / MU_SUN)
    tof_days = tof_s / 86400

    # Velocities
    v1_circ = math.sqrt(MU_SUN / r1_km)
    v2_circ = math.sqrt(MU_SUN / r2_km)
    v1_trans = math.sqrt(MU_SUN * (2 / r1_km - 1 / a_t_km))
    v2_trans = math.sqrt(MU_SUN * (2 / r2_km - 1 / a_t_km))
    dv1 = abs(v1_trans - v1_circ)
    dv2 = abs(v2_circ - v2_trans)

    # C3 (departure)
    v_inf_dep = dv1
    c3 = v_inf_dep**2

    # Phase angle
    phase_angle = 180 * (1 - (1 / (2 * math.sqrt(2))) * ((r1_au / r2_au + 1) ** 1.5 if r2_au > r1_au else (r2_au / r1_au + 1) ** 1.5))

    # Synodic period
    T1 = 2 * math.pi * math.sqrt(r1_km**3 / MU_SUN) / 86400
    T2 = 2 * math.pi * math.sqrt(r2_km**3 / MU_SUN) / 86400
    synodic_days = abs(1 / (1/T1 - 1/T2)) if T1 != T2 else float('inf')

    return {
        "tof_days": tof_days,
        "dv1_kms": dv1,
        "dv2_kms": dv2,
        "dv_total_kms": dv1 + dv2,
        "c3_km2s2": c3,
        "v_inf_dep_kms": v_inf_dep,
        "v_inf_arr_kms": abs(v2_trans - v2_circ),
        "phase_angle_deg": phase_angle,
        "synodic_period_days": synodic_days,
        "synodic_period_years": synodic_days / 365.25,
    }


