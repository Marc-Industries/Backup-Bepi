def plot_total_mission(phases):
    _PHASE_COLORS = [
        "cyan", "lime", "orange", "magenta", "yellow",
        "deepskyblue", "coral", "springgreen", "mediumpurple", "tomato",
    ]

    has_helio = any(p.get("phase_type") in ("TRANSFER", "ESCAPE", "FLYBY") for p in phases)

    if has_helio:
        return _plot_mission_heliocentric(phases, _PHASE_COLORS)
    else:
        return _plot_mission_single_body(phases, _PHASE_COLORS)


def _plot_mission_single_body(phases, colors):
    body_name = phases[0].get("body", "Earth") if phases else "Earth"
    b = get_body(body_name)
    fig = go.Figure()
    for t in _body_traces(body_name):
        fig.add_trace(t)

    max_a = b.radius_km
    cum_dv = 0.0
    for i, ph in enumerate(phases):
        color = colors[i % len(colors)]
        fp = ph.get("full_orbital_params", {})
        alt = fp.get("alt_km", 400)
        inc = fp.get("inc_deg", 0)
        ecc = fp.get("ecc", 0)
        raan = fp.get("raan_deg", 0)
        aop = fp.get("aop_deg", 0)
        a = b.radius_km + alt
        max_a = max(max_a, a * (1 + ecc))

        xs, ys, zs = _keplerian_orbit(a, ecc, inc, raan, aop)
        fig.add_trace(go.Scatter3d(
            x=xs, y=ys, z=zs, mode="lines",
            line=dict(color=color, width=2.5),
            name=f"{ph.get('name', f'Phase {i+1}')}",
        ))

        dv = ph.get("delta_v_ms", 0)
        if dv > 0:
            cum_dv += dv
            fig.add_trace(go.Scatter3d(
                x=[xs[0]], y=[ys[0]], z=[zs[0]], mode="markers+text",
                marker=dict(size=6, color="red", symbol="diamond"),
                text=[f"ΔV={dv:.0f} m/s"], textposition="top center",
                textfont=dict(color="red", size=10),
                showlegend=False,
            ))

    fig.update_layout(
        title=f"Mission Profile — {len(phases)} phases, ΔV_total={cum_dv:.0f} m/s",
        **_scene_layout(max_a * 1.5),
    )
    return fig


def _plot_mission_heliocentric(phases, colors):
    fig = go.Figure()

    fig.add_trace(go.Scatter3d(
        x=[0], y=[0], z=[0], mode="markers",
        marker=dict(size=14, color="yellow"), name="Sun",
    ))

    bodies_shown = set()
    theta = np.linspace(0, 2 * np.pi, 200)
    max_r = 0

    cum_dv = 0.0
    prev_body = None
    prev_angle = 0.0

    for i, ph in enumerate(phases):
        color = colors[i % len(colors)]
        body_name = ph.get("body", "Earth")
        phase_type = ph.get("phase_type", "PARKING_ORBIT")
        dv = ph.get("delta_v_ms", 0)
        cum_dv += dv

        if phase_type in ("TRANSFER", "ESCAPE", "FLYBY"):
            dep_body = prev_body or "Earth"
            arr_body = body_name if phase_type != "ESCAPE" else body_name
            dep_b = get_body(dep_body)
            arr_b = get_body(arr_body if arr_body != dep_body else body_name)

            r1 = dep_b.sma_au
            r2 = arr_b.sma_au
            if r1 > 0 and r2 > 0:
                a_t = (r1 + r2) / 2
                ecc_t = abs(r2 - r1) / (r2 + r1)
                n_half = 180
                nu_arr = np.linspace(0, np.pi, n_half)
                p = a_t * (1 - ecc_t**2)
                r_vals = p / (1 + ecc_t * np.cos(nu_arr))
                # Rotate transfer to start from departure body angle
                start_angle = prev_angle
                angles = start_angle + nu_arr * (1 if r2 > r1 else -1)
                tx = r_vals * np.cos(angles)
                ty = r_vals * np.sin(angles)
                fig.add_trace(go.Scatter3d(
                    x=tx, y=ty, z=np.zeros(n_half),
                    mode="lines", line=dict(color=color, width=3, dash="dash"),
                    name=ph.get("name", f"Transfer {i+1}"),
                ))
                if dv > 0:
                    fig.add_trace(go.Scatter3d(
                        x=[tx[0]], y=[ty[0]], z=[0], mode="markers+text",
                        marker=dict(size=5, color="red", symbol="diamond"),
                        text=[f"ΔV={dv:.0f} m/s"], textposition="top center",
                        textfont=dict(color="red", size=9), showlegend=False,
                    ))
                prev_angle = angles[-1]
                max_r = max(max_r, r1, r2)
        else:
            b = get_body(body_name)
            r_au = b.sma_au
            if r_au > 0 and body_name not in bodies_shown:
                bodies_shown.add(body_name)
                fig.add_trace(go.Scatter3d(
                    x=r_au * np.cos(theta), y=r_au * np.sin(theta), z=np.zeros_like(theta),
                    mode="lines", line=dict(color=b.color, width=1), opacity=0.4,
                    name=f"{body_name} orbit", showlegend=False,
                ))
                bx = r_au * math.cos(prev_angle)
                by = r_au * math.sin(prev_angle)
                fig.add_trace(go.Scatter3d(
                    x=[bx], y=[by], z=[0], mode="markers+text",
                    marker=dict(size=8, color=b.color),
                    text=[body_name], textposition="top center",
                    textfont=dict(color=b.color, size=10), showlegend=False,
                ))
                max_r = max(max_r, r_au)

        prev_body = body_name

    max_r_ax = max_r * 1.4 if max_r > 0 else 2.0
    axis = dict(
        range=[-max_r_ax, max_r_ax], showbackground=True,
        backgroundcolor="rgb(5,5,20)", gridcolor="rgba(100,100,150,0.2)",
        showticklabels=True, title="AU",
    )
    fig.update_layout(
        title=f"Mission Overview — {len(phases)} phases, ΔV_total={cum_dv:.0f} m/s",
        scene=dict(xaxis=axis, yaxis=dict(**axis), zaxis=dict(**axis),
                   aspectmode="cube", camera=dict(eye=dict(x=0, y=0, z=2.5))),
        paper_bgcolor="rgb(5,5,20)", plot_bgcolor="rgb(5,5,20)",
        font=dict(color="white"), margin=dict(l=0, r=0, t=40, b=0),
        legend=dict(font=dict(color="white"), bgcolor="rgba(10,10,30,0.85)", bordercolor="rgba(100,100,150,0.5)", borderwidth=1),
    )
    return fig
