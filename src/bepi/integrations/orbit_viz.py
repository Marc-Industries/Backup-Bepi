def plot_hohmann_transfer(alt_initial_km, alt_final_km, inclination_deg=0, body="Earth"):
    b = get_body(body)
    r1 = b.radius_km + alt_initial_km
    r2 = b.radius_km + alt_final_km
    a_t = (r1 + r2) / 2
    mu = b.mu_km3s2

    v1_circ = math.sqrt(mu / r1)
    v2_circ = math.sqrt(mu / r2)
    v1_trans = math.sqrt(mu * (2 / r1 - 1 / a_t))
    v2_trans = math.sqrt(mu * (2 / r2 - 1 / a_t))
    dv1 = abs(v1_trans - v1_circ)
    dv2 = abs(v2_circ - v2_trans)
    tof_s = math.pi * math.sqrt(a_t**3 / mu)

    fig = go.Figure()
    for t in _body_traces(body):
        fig.add_trace(t)

    # Initial orbit
    x1, y1, z1 = _keplerian_orbit(r1, 0, inclination_deg, 0, 0)
    fig.add_trace(go.Scatter3d(
        x=x1, y=y1, z=z1, mode="lines",
        line=dict(color="cyan", width=2), name=f"Initial ({alt_initial_km:.0f} km)",
    ))

    # Final orbit
    x2, y2, z2 = _keplerian_orbit(r2, 0, inclination_deg, 0, 0)
    fig.add_trace(go.Scatter3d(
        x=x2, y=y2, z=z2, mode="lines",
        line=dict(color="lime", width=2), name=f"Final ({alt_final_km:.0f} km)",
    ))

    # Transfer ellipse (half orbit: ν = 0 → π, periapsis to apoapsis)
    ecc_t = (r2 - r1) / (r2 + r1)
    inc_r = math.radians(inclination_deg)
    cos_i, sin_i = math.cos(inc_r), math.sin(inc_r)
    p_t = a_t * (1 - ecc_t**2)
    n_half = 180
    nu_arr = np.linspace(0, np.pi, n_half)
    xt, yt, zt = [], [], []
    for nu in nu_arr:
        r_t = p_t / (1 + ecc_t * math.cos(nu))
        xp, yp = r_t * math.cos(nu), r_t * math.sin(nu)
        xt.append(xp)
        yt.append(yp * cos_i)
        zt.append(yp * sin_i)
    xt, yt, zt = np.array(xt), np.array(yt), np.array(zt)
    fig.add_trace(go.Scatter3d(
        x=xt, y=yt, z=zt, mode="lines",
        line=dict(color="orange", width=3, dash="dash"), name="Transfer Ellipse",
    ))

    # Burn markers — lift along radial direction so they clear the body surface
    def _lift(px, py, pz, lift_frac=0.08):
        r = math.sqrt(px**2 + py**2 + pz**2) or 1.0
        s = 1.0 + lift_frac
        return px * s, py * s, pz * s
    b1x, b1y, b1z = _lift(xt[0], yt[0], zt[0], 0.12)
    b2x, b2y, b2z = _lift(xt[-1], yt[-1], zt[-1], 0.05)
    fig.add_trace(go.Scatter3d(
        x=[b1x], y=[b1y], z=[b1z], mode="markers",
        marker=dict(size=7, color="red", symbol="diamond"),
        name=f"Burn 1: ΔV₁ = {dv1:.4f} km/s",
    ))
    fig.add_trace(go.Scatter3d(
        x=[b2x], y=[b2y], z=[b2z], mode="markers",
        marker=dict(size=7, color="yellow", symbol="diamond"),
        name=f"Burn 2: ΔV₂ = {dv2:.4f} km/s",
    ))

    fig.update_layout(
        title=f"{body} Hohmann: {alt_initial_km:.0f} → {alt_final_km:.0f} km  |  ΔV_tot = {dv1 + dv2:.4f} km/s",
        **_scene_layout(r2 * 1.5),
    )

    info = {
        "dv1_kms": dv1, "dv2_kms": dv2, "dv_total_kms": dv1 + dv2,
        "tof_seconds": tof_s, "tof_minutes": tof_s / 60, "tof_hours": tof_s / 3600,
        "a_transfer_km": a_t, "ecc_transfer": ecc_t,
        "v1_circ_kms": v1_circ, "v2_circ_kms": v2_circ,
    }
    return fig, info


